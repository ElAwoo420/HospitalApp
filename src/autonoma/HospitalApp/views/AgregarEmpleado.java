    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtNombre.getText();
        String documento = txtDocumento.getText();
        String edadStr = txtEdad.getText();
        

        try {
    int edad = Integer.parseInt(edadStr);

    Empleado nuevo = new EmpleadoSalud(nombre, documento, edad);
    hospital.agregarEmpleado(nuevo);

    JOptionPane.showMessageDialog(this, "Empleado agregado correctamente");
    this.dispose();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Edad debe ser un nÃºmero vÃ¡lido.");
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error al agregar empleado: " + e.getMessage());
}

    }//GEN-LAST:event_btnAgregarActionPerformed

